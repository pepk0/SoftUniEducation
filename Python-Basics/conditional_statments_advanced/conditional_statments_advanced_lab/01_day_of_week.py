day = int(input())
day_of_week = ""

if day == 1:
    day_of_week = "Monday"

elif day == 2:
    day_of_week = "Tuesday"

elif day == 3:
    day_of_week = "Wednesday"

elif day == 4:
    day_of_week = "Thursday"

elif day == 5:
    day_of_week = "Friday"

elif day == 6:
    day_of_week = "Saturday"

elif day == 7:
    day_of_week = "Sunday"

else:
    day_of_week = "Error"

print(day_of_week)